#include <stdio.h>

void	ft_rev_int_tab(int *tab, int size)
{
	int	a;
	int	c;
	int	x;

	a = 0;
	c = size - 1;
	while (a <= c)
	{
		x = tab[a];
		tab[a] = tab[c];
		tab[c] = x;
		a++;
		c--;
	}
}
